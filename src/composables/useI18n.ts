export function useI18n() {
  function t(key: string): string {
    return messages[locale.value]?.[key] || messages.en[key] || key;
  }

  function setLocale(lang: string) {
    locale.value = lang;
    localStorage.setItem("locale", lang);
  }

  return { t, locale, setLocale };
}
